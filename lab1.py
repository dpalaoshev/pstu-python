import json

# JSON соседей животных в зоопарке
json_data = '''
{
  "животные": [
    {
      "вид": "слон",
      "соседи": ["жираф", "носорог", "лев"]
    },
    {
      "вид": "жираф",
      "соседи": ["слон", "зебра", "обезьяна"]
    },
    {
      "вид": "лев",
      "соседи": ["слон", "тигр", "гепард"]
    },
    {
      "вид": "зебра",
      "соседи": ["жираф", "антилопа", "слон"]
    },
    {
      "вид": "обезьяна",
      "соседи": ["жираф", "тигр", "лев"]
    },
    {
      "вид": "тигр",
      "соседи": ["лев", "обезьяна", "заяц"]
    },
    {
      "вид": "антилопа",
      "соседи": ["зебра", "носорог", "крокодил"]
    },
    {
      "вид": "крокодил",
      "соседи": ["антилопа", "носорог", "птица"]
    },
    {
      "вид": "птица",
      "соседи": ["крокодил", "обезьяна", "носорог"]
    },
    {
      "вид": "носорог",
      "соседи": ["слон", "антилопа", "крокодил"]
    }
  ]
}
'''

search_animal = "слон"
data = json.loads(json_data)

for animal in data['животные']:
    if animal['вид'] == search_animal:
        neighbors = animal['соседи']
        print(f"Соседи {search_animal} в зоопарке: {', '.join(neighbors)}")
        break
else:
    print(f"Информация о соседях {search_animal} не найдена")
